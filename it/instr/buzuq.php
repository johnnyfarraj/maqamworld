                    <p>Il buzuq è uno strumento a corda della famiglia degli <a href="oud.php">Oud</a> con un piccolo corpo e un lungo manico avente tasti. È simile, nella forma, al bouzouki, strumento greco, e al saz, strumento turco. Il buzuq è originariamente uno strumento popolare del mediterraneo orientale (Palestina, Giordania, Libano e Siria) e viene spesso suonato da solo. Viene comunque inserito nella lista degli altri strumenti tradizionali in quanto è stato usato nella musica pop araba nel corso del XX secolo. Da allora è stato usato in molte registrazione e concerti diventando parte del complesso musicale arabo.
                    </p>
                    <p>
                    Il buzuq tradizionale è formato da due insiemi di corde metalliche, una doppia (C4) e una tripla (G3), suonato da un sottile pezzo di corno o un plettro di plastica. I buzuq al giorno d'oggi invece sono formati da tre insiemi di corde in modo da aumentarne l'estensione. Le corde metalliche, insieme alle chiavi di accordatura metalliche (identiche a quelle della chitarra) danno allo strumento un suono brillante, mentre i tasti sul manico coprono la maggior parte dei quarti di tono. Questo, e il fatto che il musicista possa muovere leggermente i tasti in modo da aggiustare la tonalità, fanno si che il buzuq possa suonare la maggior parte delle scale dei <a href="../maqam.php">maqam</a> con una buona precisione.
                    </p>

