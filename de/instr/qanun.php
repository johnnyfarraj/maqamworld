                    <p> Das Kanun, im Kontext arabischer Musik oft auch Qanun geschrieben (das grammatikalische Geschlecht ist im Arabischen das Maskulinum), ist ein Nachkomme der alten ägyptischen Harfe. Es spielt in der arabischen Musik seit dem zehnten Jahrhundert eine wesentliche Rolle. Das Wort <em>qanun</em> bedeutet auf Arabisch Gesetz oder Regelwerk. Im Deutschen lebt es im Wort Kanon weiter. Diese Bezeichnung wurde dem Instrument vermutlich gegeben, weil das Kanun die Stimmhöhe und damit die Stimmregel für die Instrumentalisten und Sänger festsetzt.
                    </p>
                    <p>Der Korpus des Kanun besteht aus einem trapezförmigen flachen Holzbrett, über das 81 Saiten in Dreiergruppen gespannt sind, so dass jede Note mit drei Saiten gespielt wird. Das Instrument wird flach auf das Knie des Musikers oder auf einen kleinen Tisch gelegt. Kanunsaiten werden mit zwei Plektren gespielt. Ein Plektrum wird auf den Zeigefinger der linken und eines auf den Zeigefinger der rechten Hand aufgesetzt. Eine lange Brücke auf der rechten Seite des Instruments ruht auf Fenstern, die mit Ziegen- oder Fischhaut bespannt sind, die die Resonanz des Instruments befördern. Auf der linken Seite werden die Saiten über eine Reihe von Messinghebeln geführt, die für die Mikrotonunterschiede benötigt werden.
                    </p>
                    <p>Da das Kanun nur acht Noten (in Dreiergruppen) pro Oktave hat, legt der Musiker zu Beginn die Hebel so um, wie er sie für den Anfang des <a href="../maqam.php">Maqams</a> braucht. Wenn er dann in einen anderen <a href="../maqam.php">Maqam</a> wechselt, muss er einige Hebel bewegen, während er mit der rechten Hand weiterspielt. Schnelle Tonhöhenveränderungen können auch erzielt werden, indem man mit dem linken Fingernagel kurzzeitig auf die Saite drückt.
                    </p>

