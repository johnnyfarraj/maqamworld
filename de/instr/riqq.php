                    <p>
                        Das Riq (im Arabischen maskulin) ist eine kleine Schellentrommel. Sie wird je nach Spielweise in einer oder beiden Händen gehalten. Der Rahmen besteht zumeist aus Holz und hat einen Durchmesser von rund 25 Zentimetern. Traditionell ist das Riq mit Fischhaut bespannt, neuere Instrumente nutzen jedoch oft Plastik (genauer: Mylar). Charakteristisch für den Klang des Riq sind die Schellen. Sie sind aus Messing gefertigt und etwa 0,8 bis 0,9 Millimeter dick. Sie kommen in fünf oder sechs Viergruppen daher.
                    </p>
                    <p>
                        Mit dem Riq werden ziemlich filigrane Muster und Verzierungen gespielt, bei denen eine komplizierte Fingertechnik zum Einsatz kommt. Mit dieser lassen sich eine Vielzahl von Sounds kreieren, bei denen Fell, Rand und Schellen benutzt werden.
                    </p>
                    <p>
                        In einem Takht, also einer arabischen Kammerformation, ist das Riq gewöhnlich das einzige Perkussionsinstrument. Der Riq-Spieler heißt dort Dabet al-Iqa‘, Gebieter über den Rhythmus.
                    </p>

