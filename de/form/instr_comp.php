                    <p>
                    Der Darij heißt formell eigentlich <em>Sama'i Darij</em> und ist eine einfache Instrumentalform, die nach dem <a href="../iqaa/samai_darij.php">Iqa‘ Sama‘i Darij (3/4)</a> benannt ist. Dieses Dreivierteltaktmuster liegt ihm auch zugrunde. Wie die <a href="ottoman.php">ottomanischen Instrumentalformen</a> ist der Darij in einem <a href="../maqam.php">Maqam</a> verankert, dessen Namen und Komponistennamen er trägt. Vor dem zwanzigsten Jahrhundert waren Darij-Kompositionen in der arabischen Welt sehr verbreitet. Heutzutage werden sie höchst selten aufgeführt und komponiert.
                    </p>
                    <p>
                    Der berühmteste Darij ist eine Komposition des syrischen Violinisten Sami al-Shawwa im Maqam Bayati.
