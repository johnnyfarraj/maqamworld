                    <p>
                        El Riqq es un pequeño tambor de marco de mano con platillos. El marco generalmente está hecho de madera (existen algunos modelos de aluminio) y tiene un diámetro de 9 a 10 pulgadas. Tradicionalmente está equipado con piel de pescado, aunque los riqqs modernos usan plástico (Mylar) en su lugar. Los platillos le dan al riqq su sonido distintivo. Están hechos de latón (alrededor de 0,8 mm a 0,9 mm de espesor) y vienen en 5 (a veces 6) grupos de cuatro.
                    </p>
                    <p>
                        El riqq se puede utilizar para reproducir <a href="../iqaa.php">ciclos</a> y ornamentaciones rítmicas muy complejas utilizando una técnica de digitación compleja, y puede producir una gran variedad de sonidos utilizando la piel, el marco de madera y los platillos.
                    </p>
                    <p>
                        El riqq suele ser el único instrumento de percusión en un <em>takht</em> (grupo de cámara árabe tradicional) y su reproductor se llama <em>dabet el iqa</em> (conductor del ritmo). 
                    </p>

