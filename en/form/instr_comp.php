                    <div id="tahmila" class="heading jins text-center full-width">
                        <h3>The Tahmila</h3>
                    </div>
                    <div class="text-center mt-2 mb-3 mobile-left">
                        <a href="#" class="pronunciation btn btn-default" data-audio="/audio/name/tahmila.mp3"><i class="fa fa-fw fa-volume-up"></i> Pronunciation of Tahmila</a>
